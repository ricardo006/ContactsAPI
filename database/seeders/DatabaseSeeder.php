    {
        // Verifica se o usuário já existe
        if (!User::where('email', 'test@example.com')->exists()) {
            User::factory()->create([
                'name' => 'Test User',
                'email' => 'test@example.com',
            ]);
        }

        $this->call([
            ContactTypeSeeder::class,
        ]);
    }
}
